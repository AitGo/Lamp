public abstract class MyBaseAdapter extends BaseAdapter {

    private List mList;

    public MyBaseAdapter(List list) {
        this.mList = list;
    }

    @Override
    public int getCount() {
        return mList.size();
    }

    @Override
    public Object getItem(int position) {
        return mList.get(position);
    }

    @Override
    public long getItemId(int position) {
        return position;
    }

}
